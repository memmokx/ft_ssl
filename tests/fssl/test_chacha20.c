#define TEST_CHACHA20(name, key, nonce, pt, _size, expected)                         \
  do {                                                                               \
    fssl_chacha20_ctx ctx;                                                           \
    uint8_t out[1024] = {};                                                          \
                                                                                     \
    cr_assert_eq(fssl_chacha20_init(&ctx, key), FSSL_SUCCESS, name ": init failed"); \
                                                                                     \
    fssl_slice_t iv = {.data = (uint8_t*)nonce, .size = FSSL_CHACHA20_IV_SIZE};      \
    cr_assert_eq(fssl_chacha20_set_iv(&ctx, &iv), FSSL_SUCCESS,                      \
                 name ": set_iv failed");                                            \
                                                                                     \
    fssl_chacha20_stream(&ctx, pt, out, _size);                                      \
                                                                                     \
    for (size_t i = 0; i < (_size); ++i) {                                           \
      cr_assert_eq(out[i], expected[i],                                              \
                   name                                                              \
                   ": ciphertext byte %zu mismatch: got 0x%02x, expected 0x%02x",    \
                   i, out[i], expected[i]);                                          \
    }                                                                                \
                                                                                     \
    cr_assert_eq(fssl_chacha20_init(&ctx, key), FSSL_SUCCESS,                        \
                 name ": init for decrypt failed");                                  \
    cr_assert_eq(fssl_chacha20_set_iv(&ctx, &iv), FSSL_SUCCESS,                      \
                 name ": set_iv for decrypt failed");                                \
                                                                                     \
    uint8_t d[1024] = {};                                                            \
    fssl_chacha20_stream(&ctx, out, d, _size);                                       \
                                                                                     \
    for (size_t i = 0; i < (_size); ++i) {                                           \
      cr_assert_eq(d[i], pt[i],                                                      \
                   name                                                              \
                   ": decrypted byte %zu mismatch: got 0x%02x, expected 0x%02x",     \
                   i, d[i], pt[i]);                                                  \
    }                                                                                \
  } while (0)

